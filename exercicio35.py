dia = int(input("Digite qual o dia da semana você gostaria de pesquisar: ")) # guandando valor na variável

sem: str = ["Domingo", "Segunda", "Terça", "Quarta", "Quinta", "Sexta", "Sábado"] # definindo vetores
num: int = [ 1,         2,         3,       4,        5,        6,       7]

if (dia > 0) and (dia <= 7): # se o dia for de 1 a 7
    for i in range(7): # laço de repetição
        if dia == num[i]: # se o número for o mesmo do vetor
            print("O dia da semana é: ",sem[i]) # exibir o vetor dos dias de semana
else: # senao
    print("[Valor Inválido]")